"""Decode a Minecraft Update Tags packet (packet ID 0x0d)."""


def decode_varint(data, offset):
    """Decode a Minecraft VarInt from bytes, return (value, new_offset)."""
    value = 0
    shift = 0
    while True:
        b = data[offset]
        value |= (b & 0x7F) << shift
        shift += 7
        offset += 1
        if not (b & 0x80):
            break
    return value, offset


def decode_string(data, offset):
    """Decode a length-prefixed string (VarInt length + UTF-8 bytes)."""
    length, offset = decode_varint(data, offset)
    s = data[offset : offset + length].decode("utf-8")
    return s, offset + length


def decode_tags_packet(data):
    """Parse the full Update Tags packet, returning structured data."""
    offset = 0

    packet_len, offset = decode_varint(data, offset)
    packet_id, offset = decode_varint(data, offset)

    assert packet_id == 0x0D, f"Expected packet ID 0x0d, got 0x{packet_id:02x}"

    num_tag_types, offset = decode_varint(data, offset)

    tag_types = {}  # tag_type_name -> { tag_name -> [values] }

    for _ in range(num_tag_types):
        type_name, offset = decode_string(data, offset)
        num_tags, offset = decode_varint(data, offset)
        tags = {}
        for _ in range(num_tags):
            tag_name, offset = decode_string(data, offset)
            num_values, offset = decode_varint(data, offset)
            values = []
            for _ in range(num_values):
                val, offset = decode_varint(data, offset)
                values.append(val)
            tags[tag_name] = values
        tag_types[type_name] = tags

    assert offset == len(data), f"Extra bytes: {len(data) - offset} unread"
    return packet_len, tag_types


